namespace Moo2U.SampleData {
    /// <summary>
    /// Specifies the string case
    /// </summary>
    public enum StringCase {
        /// <summary>
        /// Do not alter the string case
        /// </summary>
        None,

        /// <summary>
        /// Change string to upper case
        /// </summary>
        Upper,

        /// <summary>
        /// Change string to lower case
        /// </summary>
        Lower
    }
}